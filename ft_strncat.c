#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*temp;
	int		stop;

	temp = s1;
	stop = 0;
	if (n)
	{
		while (*s1)
			s1++;
		while ((n-- > 0) && *s2 != '\0')
		{
			*s1++ = *s2++;
		}
		*s1++ = '\0';
	}
	return (temp);
}
